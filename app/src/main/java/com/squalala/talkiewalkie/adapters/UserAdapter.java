package com.squalala.talkiewalkie.adapters;

import android.support.v7.widget.RecyclerView;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;

import com.squalala.talkiewalkie.R;
import com.squalala.talkiewalkie.models.User;
import com.squalala.talkiewalkie.viewholders.MessageViewHolder;
import com.squalala.talkiewalkie.viewholders.UserViewHolder;

import java.util.List;

/**
 * Created by Fayçal KADDOURI on 02/03/17.
 */

public class UserAdapter extends RecyclerView.Adapter<UserViewHolder> {

    private List<User> users;

    public UserAdapter(List<User> users) {
        this.users = users;
    }

    @Override
    public UserViewHolder onCreateViewHolder(ViewGroup parent, int viewType) {
        View view = LayoutInflater.from(parent.getContext()).inflate(R.layout.cell_user, parent, false);
        return new UserViewHolder(view);
    }

    @Override
    public void onBindViewHolder(UserViewHolder holder, int position) {
        User user = users.get(position);
        holder.bind(user);
    }

    @Override
    public int getItemCount() {
        return users.size();
    }
}
